		<div class="entry-content">
			The <b><i>International Cricket Council (ICC)</i></b> is the global governing body of cricket. It was founded as the Imperial Cricket Conference in 1909 by representatives from Australia, England and South Africa. It was renamed as the International Cricket Conference in 1965, and took up its current name in 1989.<br>

The ICC has <b><i>105 members</i></b>: <b><i>12 Full Members</i></b> that play Test matches and 93 Associate Members. The ICC is responsible for the organisation and governance of cricket's major international tournaments, most notably the Cricket World Cup. It also appoints the umpires and referees that officiate at all sanctioned Test matches, One Day International and Twenty20 Internationals. It promulgates the ICC Code of Conduct, which sets professional standards of discipline for international cricket, and also co-ordinates action against corruption and match-fixing through its Anti-Corruption and Security Unit (ACSU).<br>

The ICC does not control bilateral fixtures between member countries (which include all Test matches), it does not govern domestic cricket in member countries, and it does not make the laws of the game, which remain under the control of the Marylebone Cricket Club.	</div><!-- /entry-content -->

