    <h1>International Cricket Council</h1>
    <p>
    The <b><i>International Cricket Council (ICC)</i></b> is the global governing body of cricket. It was founded as the Imperial Cricket Conference in 1909 by representatives from Australia, England and South Africa. It was renamed as the International Cricket Conference in 1965, and took up its current name in 1989.<br>

The ICC has <b><i>105 members</i></b>: <b><i>12 Full Members</i></b> that play Test matches and 93 Associate Members. The ICC is responsible for the organisation and governance of cricket's major international tournaments, most notably the Cricket World Cup. It also appoints the umpires and referees that officiate at all sanctioned Test matches, One Day International and Twenty20 Internationals. It promulgates the ICC Code of Conduct, which sets professional standards of discipline for international cricket, and also co-ordinates action against corruption and match-fixing through its Anti-Corruption and Security Unit (ACSU).<br>

The ICC does not control bilateral fixtures between member countries (which include all Test matches), it does not govern domestic cricket in member countries, and it does not make the laws of the game, which remain under the control of the Marylebone Cricket Club.

The Chairman heads the board of directors and on <b><i>26 June 2014</i></b>, N.Srinivasan, the former president of BCCI, was announced as the first chairman of the council. The role of ICC president has become a largely honorary position since the establishment of the chairman role and other changes were made to the ICC constitution in 2014. It has been claimed that the 2014 changes have handed control to the so-called 'Big Three' nations of <b><i> England</i></b>, India and Australia. The last ICC president was Zaheer Abbas, who was appointed in June 2015 following the resignation of Mustafa Kamal in April 2015. The post of ICC president was abolished in April 2016 and Shashank Manohar who replaced Mr. Srinivasan in October 2015 became the first independent chairman of the ICC since then. The current CEO is <b><i>Manu Sawhney</i></b>,the former CEO of Singapore Sports Hub and Managing Director of ESPN Star Sports (ESS) 
    </p>
    
