    <h1>Pitches and  their  effect  on  bowling  in the game   of cricket</h1>
    <p>
        In the game of cricket, the cricket pitch consists of the central strip of the cricket field between the wickets. It is <b><i>22 yards (20.12 m)</i></b> long and 10 feet (3.05 m) wide. The surface is flat and normally covered with extremely short grass though this grass is soon removed by wear at the ends of the pitch.<br>

In amateur matches in some parts of the world, artificial pitches are sometimes used. These can be a slab of concrete overlaid with a coir mat or artificial turf. sometimes dirt is put over the coir mat to provide an authentic feeling pitch. Artificial pitches are rare in professional cricket, being used only when exhibition matches are played in regions where cricket is not a common sport.

The pitch has specific markings delineating the creases, as specified by the Laws of Cricket.<br><br>
        <img src="pitch.jpg" height="245px" width="550px" style="margin-left: 400px;"><br><br><br>

The word wicket often occurs in reference to the pitch. Although technically incorrect according to the Laws of Cricket (Law 6 covers the pitch and Law 8 the wickets, distinguishing between them), cricket players, followers, and commentators persist in the usage, with context eliminating any possible ambiguity. Track is yet another synonym for pitch.

The rectangular central area of the cricket field – the space used for pitches – is known as the square. Cricket pitches are usually oriented as close to the north-south direction as practical, because the low afternoon sun would be dangerous for a batsman facing due west.
     It is just the track on which the ‘bowler’ throws the ball with a straight arm which can bend to the maximum by 15 degrees to the ‘batsman’ who would basically try to hit the ball to boundary of the ground and also take care that the ‘stumps' (which is behind him by few feets) remains intact in its place.<br>
        In the<b><i> 500 years</i></b> of cricket, it is believed that the pitch plays a major part in assisting the bowler.<br><br> There are basically three types of bowlers which have legitimate bowling action to bowl in a cricket match. They are :</p>
    
        <ul style="list-style-type:disc; color:darkred; font-size:25px;">
           <li><b><i>Fast Bowler:</i></b> They are the ‘fire of cricket'. There pace is their key. They can trick the opponent with their pace. If swing is provided, they can be very lethal.</li> 
            <li><b><i>Medium Pacer:</i></b> They lack pace in their bowling. Their skill is the weapon for them. They can swing the ball. They can create a deviation in the ball which can deceive the batsman to take their wicket.</li>
            <li><b><i>Spinners:</i></b> They are ones who maintains the balance between bat and ball in cricket. They have mastered the trick to turn the ball. On their day, they can ‘turn’ the tide in the favour of their team.</li>
        
    </ul>
    
    <h2>Types of Cricket Pitches</h2>
    <h3>Dusty Pitches</h3>
    <p>
    A dusty pitch is dressed in loose clay or sand, which helps the spinners get a good amount of spin and bounce from the pitches. These types of pitches are generally formed when the pitch is not rolled properly.
    </p>
    <h3>Green Tops</h3>
    <p>
    Theses assist pacers by providing them with ample swing and seam movement from the turf. Playing on a green cricket pitch is a challenge for even the best of the batsmen as they have to judge the movement of the ball after pitching in a short time. The matches played on these cricket pitches are generally low scoring.
    </p>
    <h3>Dead Pitches</h3>
    <p>
    These pitches don’t offer any help to pacers or spinners and are used in high scoring matches. Often used in T20 cricket, these pitches favour the batsmen for the whole game.
    </p>
    <h1>Batting Area In Cricket</h1>
    <p>
    Batting is one of the central aspects of the sport of cricket. It may look simple, but becoming a competent batsman requires lots of hard work. Take some time to really dial in your stance, grip, and swing, along with other basic cues. When it comes time to take to the pitch, watch the bowler closely to try to predict his angle of delivery and keep your eye on the ball until you make contact. With enough practice, you can score more runs and take your game to the next level.
    </p>
    <h2>How to Improve Your Batting in Cricket</h2>
    <h3> 1.  Getting into Position</h3>
    <p>
        <img src="batting1.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
    
   <b><i> Take a firm grip on the bat with both hands.    </i></b> Wrap both hands around the handle of the bat with your dominant hand closest to the blade. For maximum control and precision, place your hands somewhere between the middle and top of the handle and keep them close enough together for the space between the thumb of your upper hand and the forefinger of your lower hand to form a 'V' shape.<br><br>
        -> The further away you place your hands on the handle, the more awkward maneuvering the bat will become.<br><br>
<img src="batting2.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
<b><i>Assume a comfortable stance.   </i></b>Most batsmen prefer a side-on stance. If you’re right-handed, turn your left side toward the bowler with the bat off to your right side. If you’re left-handed, simply reverse this position. Plant your feet about shoulder width and put a little bend in your knees. Keep your weight distributed evenly on the balls of your feet.<br><br>
        -> Once you’re in a basic side-on stance, angle your feet or upper body to get into a position that feels more natural.<br>
        -> There’s no single best batting stance for cricket. The important thing is that you choose a stance that offers maximum stability and leverage so you can hit with more force.<br>
        -> Be prepared to tweak your stance during games to adapt to different shots. If the bowler delivers a bouncer just in front of your feet, for instance, you'll need to be ready to shift your weight and play a cut       off your back foot.<br><br>
         <img src="batting3.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
        <b><i>Chamber the bat at waist height until it’s time to swing.   </i></b>Hold the bat out to the side so that it's parallel with the ground, or keep it pointed up or down at a slight angle. The exact position of the bat doesn't matter as long as you're able to move it quickly and easily to initiate your swing.<br><br>
        -> Make sure you’re holding the bat with the flat side facing the bowler.<br>
        -> Since most bowls tend to be delivered closer to ground level, a cricket bat shouldn't be held as high as a baseball bat.<br>
    </p>
    <h3> 2.   Delivering a Proper Swing</h3><br>
    <p>
     <img src="batting4.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
        <b><i>Begin lifting the bat as the bowler delivers the ball. </i></b> Bring the bat up and back until it’s hovering just below shoulder height. From here, you’ll be able to move forward or backward while making sure the bat stays in the ready position. Keep your elbows bent and your shoulders relaxed but ready to strike.<br>
        -> If you wait until the bowler releases the ball to start pulling back, you won’t be able to reverse the motion of the bat in time to make contact at the optimal distance.<br>
        -> Avoid raising the bat at too much of an angle, as this can throw off the accuracy of your swing.<br><br>
        <img src="batting5.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
        <b><i>Swing the bat straight up to meet the ball. </i></b> Quickly bring the bat back across your body with the blade perpendicular to the ground or at a horizontal angle. Make an effort to strike the ball as close to the center of the bat as possible. This is the basic technique used for most basic shots, including drives and defensive hits.<br><br>
        -> Watch the ball closely as it crosses the pitch to determine which type of shot you'll need to play.<br>
        -> The batting action in cricket has more in common with a tight, controlled golf swing than the wide, horizontal arc of a baseball bat.<br><br>
        <img src="batting6.jpg" height="215px" width="550px" style="margin-left: 400px;"><br><br>
     <b><i>   Follow through to get more distance on your shot.</i></b> As you make contact, twist your hips and open up your chest in the direction of your swing. Continue driving the bat upwards without taking your non-dominant hand off the handle. A smooth, well-timed rotation will generate more momentum, sending the ball further.<br>
        -> An exaggerated follow through is most useful for hitting drives, and may not be necessary when you're just trying to prevent the ball from making it to the wicket.<br><br>
    </p>
