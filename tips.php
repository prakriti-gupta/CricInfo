		<div class="entry-content">
			<h3> 1.  Getting into Position</h3>
    <p>
        <img src="batting1.jpg" height="215px" width="550p"><br><br>
    
   <b><i> Take a firm grip on the bat with both hands.    </i></b> Wrap both hands around the handle of the bat with your dominant hand closest to the blade. For maximum control and precision, place your hands somewhere between the middle and top of the handle and keep them close enough together for the space between the thumb of your upper hand and the forefinger of your lower hand to form a 'V' shape.<br><br>
        -> The further away you place your hands on the handle, the more awkward maneuvering the bat will become.<br><br>
<img src="batting2.jpg" height="215px" width="550px"><br><br>
<b><i>Assume a comfortable stance.   </i></b>Most batsmen prefer a side-on stance. If you’re right-handed, turn your left side toward the bowler with the bat off to your right side. If you’re left-handed, simply reverse this position. Plant your feet about shoulder width and put a little bend in your knees. Keep your weight distributed evenly on the balls of your feet.<br><br>
        -> Once you’re in a basic side-on stance, angle your feet or upper body to get into a position that feels more natural.<br>
        -> There’s no single best batting stance for cricket. The important thing is that you choose a stance that offers maximum stability and leverage so you can hit with more force.<br>
        -> Be prepared to tweak your stance during games to adapt to different shots. If the bowler delivers a bouncer just in front of your feet, for instance, you'll need to be ready to shift your weight and play a cut       off your back foot.<br><br>
         <img src="batting3.jpg" height="215px" width="550px"><br><br>
        <b><i>Chamber the bat at waist height until it’s time to swing.   </i></b>Hold the bat out to the side so that it's parallel with the ground, or keep it pointed up or down at a slight angle. The exact position of the bat doesn't matter as long as you're able to move it quickly and easily to initiate your swing.<br><br>
        -> Make sure you’re holding the bat with the flat side facing the bowler.<br>
        -> Since most bowls tend to be delivered closer to ground level, a cricket bat shouldn't be held as high as a baseball bat.<br>
    </p>
    <h3> 2.   Delivering a Proper Swing</h3><br>
    <p>
     <img src="batting4.jpg" height="215px" width="550px"><br><br>
        <b><i>Begin lifting the bat as the bowler delivers the ball. </i></b> Bring the bat up and back until it’s hovering just below shoulder height. From here, you’ll be able to move forward or backward while making sure the bat stays in the ready position. Keep your elbows bent and your shoulders relaxed but ready to strike.<br>
        -> If you wait until the bowler releases the ball to start pulling back, you won’t be able to reverse the motion of the bat in time to make contact at the optimal distance.<br>
        -> Avoid raising the bat at too much of an angle, as this can throw off the accuracy of your swing.<br><br>
        <img src="batting5.jpg" height="215px" width="550px"><br><br>
        <b><i>Swing the bat straight up to meet the ball. </i></b> Quickly bring the bat back across your body with the blade perpendicular to the ground or at a horizontal angle. Make an effort to strike the ball as close to the center of the bat as possible. This is the basic technique used for most basic shots, including drives and defensive hits.<br><br>
        -> Watch the ball closely as it crosses the pitch to determine which type of shot you'll need to play.<br>
