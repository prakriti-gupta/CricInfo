			In the game of cricket, the cricket pitch consists of the central strip of the cricket field between the wickets. It is <b><i>22 yards (20.12 m)</i></b> long and 10 feet (3.05 m) wide. The surface is flat and normally covered with extremely short grass though this grass is soon removed by wear at the ends of the pitch.<br>

In amateur matches in some parts of the world, artificial pitches are sometimes used. These can be a slab of concrete overlaid with a coir mat or artificial turf. sometimes dirt is put over the coir mat to provide an authentic feeling pitch. Artificial pitches are rare in professional cricket, being used only when exhibition matches are played in regions where cricket is not a common sport.

The pitch has specific markings delineating the creases, as specified by the Laws of Cricket.
        <img src="pitch.jpg" height="50px" width="500px">

The word wicket often occurs in reference to the pitch. Although technically incorrect according to the Laws of Cricket (Law 6 covers the pitch and Law 8 the wickets, distinguishing between them), cricket players, followers, and commentators persist in the usage, with context eliminating any possible ambiguity. Track is yet another synonym for pitch.<br>

The rectangular central area of the cricket field – the space used for pitches – is known as the square. Cricket pitches are usually oriented as close to the north-south direction as practical, because the low afternoon sun would be dangerous for a batsman facing due west.
            In the<b><i> 500 years</i></b> of cricket, it is believed that the pitch plays a major part in assisting the bowler.<br><br> There are basically three types of bowlers which have legitimate bowling action to bowl in a cricket match. They are :<br><br>
    
        
           <b><i><u>Fast Bowler </u> : </i></b> They are the ‘fire of cricket'. There pace is their key. They can trick the opponent with their pace. If swing is provided, they can be very lethal.<br> 
            <b><i><u>Medium Pacer </u> : </i></b> They lack pace in their bowling. Their skill is the weapon for them. They can swing the ball. They can create a deviation in the ball which can deceive the batsman to take their wicket.<br>
            <b><i><u>Spinners </u> : </i></b> They are ones who maintains the balance between bat and ball in cricket. They have mastered the trick to turn the ball. On their day, they can ‘turn’ the tide in the favour of their team.
        
    
